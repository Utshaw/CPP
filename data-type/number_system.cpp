#include <iostream>
#include <bits/stdc++.h>

using namespace std;

#define endl '\n'

int main() {
    
    int a = 10; // decimal format
    cout << a << endl;
    a = 012; // Octal representation 10 in decimal
    cout << a << endl;
    a = 0x41; // Hex representation 65 in decimal
    cout << a << endl;


    float b = 15.25F;
    double c = 15.25; // Auto double literal
    char d = 'A'; // Character literal





    return 0;
}